package biplap.com.FirstApp;

import android.os.Bundle;
import android.widget.GridView;

import androidx.activity.EdgeToEdge;
import androidx.appcompat.app.AppCompatActivity;
import androidx.core.graphics.Insets;
import androidx.core.view.ViewCompat;
import androidx.core.view.WindowInsetsCompat;

public class GridViewExmpleActivity extends AppCompatActivity {

    GridView gridView;
    String[] languages = {"C", "C#", "C++", "Java", "Python", "JavaScript", "Ruby", "Swift", "Go", "Rust", "Kotlin"};
    int[] images = {
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img,
            R.drawable.img
    };



    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_grid_view_exmple);
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main), (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });

        gridView= findViewById(R.id.gridView);
        CustomGridAdapter customGridAdapter = new CustomGridAdapter(GridViewExmpleActivity.this,languages,images);
        gridView.setAdapter(customGridAdapter);
    }
}